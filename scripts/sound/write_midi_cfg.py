def write_midi_cfg():
    filename = "sound/songs/midi/midi.cfg"
    data = []
    if os.path.exists(filename):
        filename += ".new"
    for song in song_volumes:
        data.append([f"{song[0]}.mid:", "-l", "-R0", f"-V{song[1]:03}", "-P128"])
    content = tabulate(data, tablefmt="plain")
    with open(filename, "w", encoding="utf-8") as fp:
        fp.write(content)

